#!/usr/bin/env bash
set -euo pipefail

DATASET_NAME="${DATASET_NAME:-Ego4d}"
TS="${TS:-$(date +%Y%m%d_%H%M%S)}"
CASE_FILTER="${CASE_FILTER:-A01,A04,A06,A08,B01,B03,B05,B08,C01,C02,C03,C04}"
GPU_A="${GPU_A:-0}"
GPU_B="${GPU_B:-1}"
GPU_C="${GPU_C:-2}"
IB="${IB:-32}"
TB="${TB:-16}"
MAX_FRAMES="${MAX_FRAMES:-4000}"
GPU_MEM_UTIL="${GPU_MEM_UTIL:-0.9}"
SPEC_RAY_PORT_A="${SPEC_RAY_PORT_A:-33421}"
SPEC_RAY_PORT_B="${SPEC_RAY_PORT_B:-33422}"
SPEC_RAY_PORT_C="${SPEC_RAY_PORT_C:-33423}"
SPEC_RAY_WORKER_MIN_A="${SPEC_RAY_WORKER_MIN_A:-51000}"
SPEC_RAY_WORKER_MAX_A="${SPEC_RAY_WORKER_MAX_A:-51079}"
SPEC_RAY_WORKER_MIN_B="${SPEC_RAY_WORKER_MIN_B:-51100}"
SPEC_RAY_WORKER_MAX_B="${SPEC_RAY_WORKER_MAX_B:-51179}"
SPEC_RAY_WORKER_MIN_C="${SPEC_RAY_WORKER_MIN_C:-51200}"
SPEC_RAY_WORKER_MAX_C="${SPEC_RAY_WORKER_MAX_C:-51279}"
NOSPEC_RAY_PORT_A="${NOSPEC_RAY_PORT_A:-33521}"
NOSPEC_RAY_PORT_B="${NOSPEC_RAY_PORT_B:-33522}"
NOSPEC_RAY_PORT_C="${NOSPEC_RAY_PORT_C:-33523}"
NOSPEC_RAY_WORKER_MIN_A="${NOSPEC_RAY_WORKER_MIN_A:-52000}"
NOSPEC_RAY_WORKER_MAX_A="${NOSPEC_RAY_WORKER_MAX_A:-52079}"
NOSPEC_RAY_WORKER_MIN_B="${NOSPEC_RAY_WORKER_MIN_B:-52100}"
NOSPEC_RAY_WORKER_MAX_B="${NOSPEC_RAY_WORKER_MAX_B:-52179}"
NOSPEC_RAY_WORKER_MIN_C="${NOSPEC_RAY_WORKER_MIN_C:-52200}"
NOSPEC_RAY_WORKER_MAX_C="${NOSPEC_RAY_WORKER_MAX_C:-52279}"

TREAM_ROOT="/workspace/vllm/tream"
LAUNCHER="${TREAM_ROOT}/inference_logs/launch_ab48_trigpu_shift_taskpool.sh"
DATASET_DIR="${TREAM_ROOT}/data/streaming-lvm-dataset/${DATASET_NAME}"

if [[ ! -d "${DATASET_DIR}" ]]; then
  echo "Dataset dir not found: ${DATASET_DIR}" >&2
  exit 1
fi

BASE="${TREAM_ROOT}/f_test/${DATASET_NAME}/ab48"
SPEC_LOG_DIR="${BASE}/02_spec_only/logs/${TS}"
NOSPEC_LOG_DIR="${BASE}/01_no_feature/logs/${TS}"
mkdir -p "${SPEC_LOG_DIR}" "${NOSPEC_LOG_DIR}"

PAIR_LOG="${BASE}/00_metadata/ab12_pair_${DATASET_NAME}_${TS}.launcher.log"

echo "[$(date '+%F %T')] START AB12 ${DATASET_NAME} spec_vs_nospec" | tee "${PAIR_LOG}"
echo "[$(date '+%F %T')] CASE_FILTER=${CASE_FILTER}" | tee -a "${PAIR_LOG}"
echo "[$(date '+%F %T')] GPUS=${GPU_A},${GPU_B},${GPU_C} IB=${IB} TB=${TB} MAX_FRAMES=${MAX_FRAMES}" | tee -a "${PAIR_LOG}"

# static + spec
GPU_A="${GPU_A}" GPU_B="${GPU_B}" GPU_C="${GPU_C}" \
RAY_PORT_A="${SPEC_RAY_PORT_A}" RAY_PORT_B="${SPEC_RAY_PORT_B}" RAY_PORT_C="${SPEC_RAY_PORT_C}" \
RAY_WORKER_MIN_A="${SPEC_RAY_WORKER_MIN_A}" RAY_WORKER_MAX_A="${SPEC_RAY_WORKER_MAX_A}" \
RAY_WORKER_MIN_B="${SPEC_RAY_WORKER_MIN_B}" RAY_WORKER_MAX_B="${SPEC_RAY_WORKER_MAX_B}" \
RAY_WORKER_MIN_C="${SPEC_RAY_WORKER_MIN_C}" RAY_WORKER_MAX_C="${SPEC_RAY_WORKER_MAX_C}" \
RUN_MODE=static RUN_SPEC=1 \
CASE_FILTER="${CASE_FILTER}" \
IB="${IB}" TB="${TB}" MAX_FRAMES="${MAX_FRAMES}" GPU_MEM_UTIL="${GPU_MEM_UTIL}" \
DATASET_SUBDIR="${DATASET_NAME}" DATASET_DIR="${DATASET_DIR}" LOG_DIR="${SPEC_LOG_DIR}" \
"${LAUNCHER}" 2>&1 | tee -a "${PAIR_LOG}"

sleep 5

# static + nospec
GPU_A="${GPU_A}" GPU_B="${GPU_B}" GPU_C="${GPU_C}" \
RAY_PORT_A="${NOSPEC_RAY_PORT_A}" RAY_PORT_B="${NOSPEC_RAY_PORT_B}" RAY_PORT_C="${NOSPEC_RAY_PORT_C}" \
RAY_WORKER_MIN_A="${NOSPEC_RAY_WORKER_MIN_A}" RAY_WORKER_MAX_A="${NOSPEC_RAY_WORKER_MAX_A}" \
RAY_WORKER_MIN_B="${NOSPEC_RAY_WORKER_MIN_B}" RAY_WORKER_MAX_B="${NOSPEC_RAY_WORKER_MAX_B}" \
RAY_WORKER_MIN_C="${NOSPEC_RAY_WORKER_MIN_C}" RAY_WORKER_MAX_C="${NOSPEC_RAY_WORKER_MAX_C}" \
RUN_MODE=static RUN_SPEC=0 \
CASE_FILTER="${CASE_FILTER}" \
IB="${IB}" TB="${TB}" MAX_FRAMES="${MAX_FRAMES}" GPU_MEM_UTIL="${GPU_MEM_UTIL}" \
DATASET_SUBDIR="${DATASET_NAME}" DATASET_DIR="${DATASET_DIR}" LOG_DIR="${NOSPEC_LOG_DIR}" \
"${LAUNCHER}" 2>&1 | tee -a "${PAIR_LOG}"

echo "[$(date '+%F %T')] DONE AB12 ${DATASET_NAME} spec_vs_nospec" | tee -a "${PAIR_LOG}"
echo "SPEC_LOG_DIR=${SPEC_LOG_DIR}" | tee -a "${PAIR_LOG}"
echo "NOSPEC_LOG_DIR=${NOSPEC_LOG_DIR}" | tee -a "${PAIR_LOG}"
